



class math:
    def __init__(self,fnumber,snumber):
        self.fnumber=fnumber
        self.snumber=snumber
        
    def addition(self):
        return self.fnumber+self.snumber
    
    def multiplication(self):
        return self.fnumber*self.snumber
    
    def division(self):
        return self.fnumber/self.snumber

    def subtraction(self):
        return self.fnumber-self.snumber
    
        
        
